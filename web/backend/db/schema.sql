-- 10. Customer profile embeddings (for RAG similarity search)
CREATE TABLE CUSTOMER_EMBEDDINGS (
  EMBED_ID      NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  CUSTOMER_ID   VARCHAR2(50)  NOT NULL REFERENCES CUSTOMERS(CUSTOMER_ID),
  CONTENT_TYPE  VARCHAR2(50)  NOT NULL, -- profile / notes / products / goals
  CONTENT       CLOB          NOT NULL,
  EMBEDDING     VECTOR(1024, FLOAT32)   NOT NULL,
  MODEL_USED    VARCHAR2(200),
  CREATED_AT    TIMESTAMP     DEFAULT CURRENT_TIMESTAMP
);

CREATE VECTOR INDEX IDX_CUST_EMBED_VEC ON CUSTOMER_EMBEDDINGS(EMBEDDING)
  ORGANIZATION NEIGHBOR PARTITIONS
  WITH DISTANCE COSINE
  WITH TARGET ACCURACY 95;

-- 11. Meeting notes embeddings
CREATE TABLE MEETING_NOTES_EMBEDDINGS (
