  NOTE_ID       NUMBER        NOT NULL REFERENCES MEETING_NOTES(NOTE_ID),
  CUSTOMER_ID   VARCHAR2(50)  NOT NULL REFERENCES CUSTOMERS(CUSTOMER_ID),
  CONTENT       CLOB          NOT NULL,
  EMBEDDING     VECTOR(1024, FLOAT32) NOT NULL,
  MODEL_USED    VARCHAR2(200),
  CREATED_AT    TIMESTAMP     DEFAULT CURRENT_TIMESTAMP
);

CREATE VECTOR INDEX IDX_NOTES_EMBED_VEC ON MEETING_NOTES_EMBEDDINGS(EMBEDDING)
  ORGANIZATION NEIGHBOR PARTITIONS
  WITH DISTANCE COSINE
  WITH TARGET ACCURACY 95;

