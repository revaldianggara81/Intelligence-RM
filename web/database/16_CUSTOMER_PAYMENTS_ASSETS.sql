SELECT c.CUSTOMER_ID,
       c.FULL_NAME,
       c.MONTHLY_INCOME,
       NVL(SUM(i.AMOUNT),0) AS INCOME_SOURCE_TOTAL
FROM   CUSTOMERS c
LEFT JOIN CUSTOMER_INCOME_SOURCES i
       ON i.CUSTOMER_ID = c.CUSTOMER_ID
      AND i.IS_ACTIVE = 1
WHERE  c.CUSTOMER_ID LIKE 'CUST%'
GROUP  BY c.CUSTOMER_ID, c.FULL_NAME, c.MONTHLY_INCOME
ORDER  BY c.CUSTOMER_ID;

SELECT CUSTOMER_ID, CARD_NUMBER_MASKED, CREDIT_LIMIT, OUTSTANDING, PAYMENT_STATUS, MISSED_MONTH
FROM   CREDIT_CARDS
ORDER  BY CUSTOMER_ID;

SELECT CUSTOMER_ID, COUNT(*) AS CC_PAYMENT_ROWS
FROM   CREDIT_CARD_PAYMENTS
GROUP  BY CUSTOMER_ID
ORDER  BY CUSTOMER_ID;

SELECT CUSTOMER_ID, COUNT(*) AS DEPOSIT_PAYMENT_ROWS, SUM(PAYMENT_AMOUNT) AS TOTAL_PAYMENT_AMOUNT
FROM   DEPOSIT_PAYMENT_SCHEDULE
GROUP  BY CUSTOMER_ID
ORDER  BY CUSTOMER_ID;

SELECT CUSTOMER_ID, ASSET_TYPE, COUNT(*) AS ASSET_COUNT, SUM(ESTIMATED_VALUE) AS TOTAL_VALUE
FROM   CUSTOMER_ASSETS
WHERE  IS_ACTIVE = 1
GROUP  BY CUSTOMER_ID, ASSET_TYPE
ORDER  BY CUSTOMER_ID, ASSET_TYPE;

