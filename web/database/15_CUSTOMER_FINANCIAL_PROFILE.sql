CREATE TABLE CUSTOMER_INCOME_SOURCES (
  INCOME_ID     NUMBER         GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  CUSTOMER_ID   VARCHAR2(20)   NOT NULL REFERENCES CUSTOMERS(CUSTOMER_ID),
  SOURCE_NAME   VARCHAR2(100)  NOT NULL,  -- e.g. "Pension (BPJS)", "Rental Income"
  SOURCE_TYPE   VARCHAR2(30)   NOT NULL,  -- pension / salary / rental / business / dividend / freelance
  AMOUNT        NUMBER(20,2)   NOT NULL,
  UNIT_COUNT    NUMBER(5)      DEFAULT NULL,  -- e.g. 2 for "2 units" of rental
  ICON          VARCHAR2(10)   DEFAULT NULL,  -- emoji icon
  SORT_ORDER    NUMBER(3)      DEFAULT 1,
  IS_ACTIVE     NUMBER(1)      DEFAULT 1,
  CREATED_AT    TIMESTAMP      DEFAULT SYSTIMESTAMP
);

CREATE INDEX IDX_INCOME_CUST ON CUSTOMER_INCOME_SOURCES(CUSTOMER_ID);

