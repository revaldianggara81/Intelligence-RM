-- =============================================================================
-- VERIFICATION
-- =============================================================================

SELECT c.CUSTOMER_ID, c.FULL_NAME, c.CIF_NUMBER,
       SUM(i.AMOUNT) AS TOTAL_INCOME_FROM_SOURCES,
       c.MONTHLY_INCOME AS DB_MONTHLY_INCOME
FROM   CUSTOMERS c
LEFT JOIN CUSTOMER_INCOME_SOURCES i ON i.CUSTOMER_ID = c.CUSTOMER_ID
GROUP BY c.CUSTOMER_ID, c.FULL_NAME, c.CIF_NUMBER, c.MONTHLY_INCOME
ORDER BY c.CUSTOMER_ID;

SELECT CUSTOMER_ID, CARD_NUMBER_MASKED, OUTSTANDING, PAYMENT_STATUS, MISSED_MONTH
FROM   CREDIT_CARDS
ORDER BY CUSTOMER_ID;

