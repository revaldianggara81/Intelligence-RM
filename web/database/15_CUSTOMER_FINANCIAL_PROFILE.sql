CREATE TABLE CREDIT_CARDS (
  CARD_ID            NUMBER         GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  CUSTOMER_ID        VARCHAR2(20)   NOT NULL REFERENCES CUSTOMERS(CUSTOMER_ID),
  CARD_NUMBER_MASKED VARCHAR2(20)   NOT NULL,  -- e.g. "**** **** **** 4521"
  CARD_TYPE          VARCHAR2(30)   DEFAULT 'Visa',  -- Visa / Mastercard / JCB
  CARD_BRAND         VARCHAR2(30)   DEFAULT 'Danamon Visa',
  CREDIT_LIMIT       NUMBER(20,2)   NOT NULL,
  OUTSTANDING        NUMBER(20,2)   DEFAULT 0,
  MIN_PAYMENT        NUMBER(20,2)   DEFAULT 0,
  PAYMENT_STATUS     VARCHAR2(20)   DEFAULT 'Current',  -- Current / Missed / Late
  LAST_PAYMENT_DATE  DATE           DEFAULT NULL,
  MISSED_MONTH       VARCHAR2(20)   DEFAULT NULL,  -- e.g. "Apr 2026"
  DUE_DATE           NUMBER(2)      DEFAULT 25,   -- day of month
  IS_ACTIVE          NUMBER(1)      DEFAULT 1,
  ISSUED_DATE        DATE           DEFAULT SYSDATE,
  CREATED_AT         TIMESTAMP      DEFAULT SYSTIMESTAMP
);

CREATE INDEX IDX_CC_CUST ON CREDIT_CARDS(CUSTOMER_ID);

